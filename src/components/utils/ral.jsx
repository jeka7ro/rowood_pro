// IMPORTANT: DOAR mapare exactă. Fără fallback generativ.
export function hexFromRal(code) {
  if (!code) return null;
  const clean = String(code).replace(/[^0-9]/g, "").padStart(4, "0");
  return RAL_HEX[clean] || null;
}